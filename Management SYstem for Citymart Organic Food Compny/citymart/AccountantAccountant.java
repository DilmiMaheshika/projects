    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // Add Incoming Requsests
        
        if(jTextField26.getText().trim().isEmpty() || jTextField30.getText().trim().isEmpty() || jTextField31.getText().trim().isEmpty() || jTextField32.getText().trim().isEmpty()
               || jTextField33.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this,"Some Fields are Empty!!!", " Empty Fields" , JOptionPane.WARNING_MESSAGE);
                    
        }else{
  
         
            try {
                
                    String sql = "INSERT INTO incoming_requests VALUES (?,?,?,?,?,?,?)";
                    ps = conn.prepareStatement(sql);

                    String id = jTextField26.getText();
                    String name = jTextField30.getText();
                    String category = jComboBox3.getSelectedItem().toString();
                    int price = Integer.parseInt(jTextField31.getText());
                    int quantity = Integer.parseInt(jTextField32.getText());
                    String companyname = jTextField33.getText();
                    String approvalST = jComboBox7.getSelectedItem().toString();
                
                    
                    ps.setString(1, id);
                    ps.setString(2, name);
                    ps.setString(3, category);
                    ps.setInt(4, price);
                    ps.setInt(5, quantity);
                    ps.setString(6, companyname);
                    ps.setString(7,approvalST);
               
                    if(ps.executeUpdate()>0){
                       DefaultTableModel model= (DefaultTableModel)jTable2.getModel();
                       model.setRowCount(0);
                       Req_Details();
                       JOptionPane.showMessageDialog(this,"Request Details Added Successfully!!!");
                    }

                    IncomingReqAddClear();
            
            } catch (Exception ex) {
                System.err.println("Error :" + ex.getMessage());
            }
        }  
    }//GEN-LAST:event_jButton13ActionPerformed

