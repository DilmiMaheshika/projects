     public ArrayList<Requests> RequestIncome(){
        ArrayList<Requests> RequestsIncome =new ArrayList<>();
        try{
            String sql = "SELECT * FROM incoming_requests";
            ps=conn.prepareStatement(sql);
            ResultSet rs= ps.executeQuery();
            Requests requests;
            while(rs.next()){
                requests=new Requests(rs.getInt("Requests_No"), rs.getInt("Unit_Price_Rs"), rs.getInt("Quantity"), rs.getString("Item_Name"), rs.getString("Category"),rs.getString("Company_Name"), rs.getString("Approval_Status"));
                RequestsIncome.add(requests);
            }
        
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
        return RequestsIncome;
    }
    
   
