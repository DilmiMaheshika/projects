    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // incoming requests SEARCH delete Button
         try{
           String sql = "SELECT * FROM incoming_requests WHERE Requests_No=?";
           ps = conn.prepareStatement(sql);
           String search=jTextField2.getText();
           ps.setString(1,search);
           ResultSet rs= ps.executeQuery();
           
           if(rs.next()){
               String id=rs.getString("Requests_No");
               jTextField39.setText(id);
               
               String name=rs.getString("Item_Name");
               jTextField40.setText(name);
               
               String category=rs.getString("Category");
               switch(category){
                    case "Select Category":
                       jComboBox4.setSelectedIndex(0);
                       break;
                   case "Vegetables":
                       jComboBox4.setSelectedIndex(1);
                       break;
                   case "Fruits":
                       jComboBox4.setSelectedIndex(2);
                       break;
                   case "Leaf Varieties":
                       jComboBox4.setSelectedIndex(3);
                       break;
               }
               
               String price = rs.getString("Unit_Price_Rs");
               jTextField41.setText(price);
               
               String quantity = rs.getString("Quantity");
               jTextField42.setText(quantity);
               
                String companyname = rs.getString("Company_Name");
               jTextField43.setText(companyname);
               
                 String approvalST=rs.getString("Approval_Status");
               switch(approvalST){
                    case "None":
                       jComboBox8.setSelectedIndex(0);
                       break;
                   case "Approved":
                       jComboBox8.setSelectedIndex(1);
                       break;
                   case "Not Approved":
                       jComboBox8.setSelectedIndex(2);
                       break;
                }
               
           } else {
                JOptionPane.showMessageDialog(this, "Item Details Not Found");
                RequestDelete();
           }
            
        }catch(Exception ex){
           System.err.println("Error :" + ex.getMessage()); 
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

